function runPython(code) {
  const result = spawnSync(PYTHON, ["-c", code], {
    cwd: REPO_ROOT,
    encoding: "utf8",
  });
  if (result.status !== 0) {
    throw new Error(result.stderr || result.stdout || "Python 执行失败");
  }
}

