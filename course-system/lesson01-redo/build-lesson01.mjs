function prepareCroppedImages() {
  const specs = Object.entries(IMAGE_SPECS).map(([key, value]) => ({
    key,
    file: value.file,
    source: value.source,
    box: value.box,
  }));
  const code = `
from PIL import Image, ImageOps
import os, json
src = r"${UPRIGHT_DIR.replace(/\\/g, "\\\\")}"
out = r"${CROPPED_DIR.replace(/\\/g, "\\\\")}"
specs = json.loads(r'''${JSON.stringify(specs)}''')
os.makedirs(out, exist_ok=True)
for spec in specs:
    src_path = os.path.join(src, spec["source"])
    dst_path = os.path.join(out, spec["file"])
    if not os.path.exists(src_path):
        continue
    img = Image.open(src_path).convert("RGB")
    left, top, right, bottom = spec["box"]
    cropped = img.crop((left, top, right, bottom))
    cropped = ImageOps.autocontrast(cropped)
    cropped.save(dst_path, quality=95)
print("ok")
`;
  runPython(code);
}

