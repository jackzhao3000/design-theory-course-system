function renderDiagram(slide, frame, diagram) {
  if (!diagram) return;
  addPanel(slide, frame, { fill: COLORS.white });
  switch (diagram.kind) {
    case "steps":
      drawStepDiagram(slide, frame, diagram.items);
      break;
    case "cards":
      drawCardsDiagram(slide, { left: frame.left + 18, top: frame.top + 18, width: frame.width - 36, height: frame.height - 36 }, diagram.items);
      break;
    case "layers":
      drawLayersDiagram(slide, { left: frame.left + 18, top: frame.top + 18, width: frame.width - 36, height: frame.height - 36 }, diagram.items);
      break;
    case "matrix":
      drawMatrixDiagram(slide, { left: frame.left + 10, top: frame.top + 10, width: frame.width - 20, height: frame.height - 20 }, diagram.rows, diagram.cols, diagram.cells);
      break;
    case "compare":
      drawCompareDiagram(slide, { left: frame.left + 18, top: frame.top + 18, width: frame.width - 36, height: frame.height - 36 }, diagram.leftTitle, diagram.rightTitle, diagram.rows);
      break;
    case "timeline":
      drawTimelineDiagram(slide, { left: frame.left + 18, top: frame.top + 18, width: frame.width - 36, height: frame.height - 36 }, diagram.items);
      break;
    case "stakeholders":
      drawStakeholdersDiagram(slide, { left: frame.left + 18, top: frame.top + 18, width: frame.width - 36, height: frame.height - 36 }, diagram.center, diagram.items);
      break;
    case "pyramid":
      drawPyramidDiagram(slide, { left: frame.left + 18, top: frame.top + 18, width: frame.width - 36, height: frame.height - 36 }, diagram.items);
      break;
    case "triangle":
      drawTriangleDiagram(slide, { left: frame.left + 18, top: frame.top + 18, width: frame.width - 36, height: frame.height - 36 }, diagram.items);
      break;
    case "loop":
      drawLoopDiagram(slide, { left: frame.left + 18, top: frame.top + 18, width: frame.width - 36, height: frame.height - 36 }, diagram.items);
      break;
    default:
      drawCardsDiagram(slide, { left: frame.left + 18, top: frame.top + 18, width: frame.width - 36, height: frame.height - 36 }, diagram.items || []);
  }
}

