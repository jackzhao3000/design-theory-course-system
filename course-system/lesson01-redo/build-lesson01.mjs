async function exportQaFiles(presentation) {
  for (let i = 0; i < presentation.slides.items.length; i += 1) {
    const slide = presentation.slides.items[i];
    const stem = `slide-${String(i + 1).padStart(2, "0")}`;
    await writeBlob(path.join(PREVIEW_DIR, `${stem}.png`), await presentation.export({ slide, format: "png", scale: 1 }));
    await fs.writeFile(path.join(LAYOUT_DIR, `${stem}.layout.json`), await (await slide.export({ format: "layout" })).text(), "utf8");
  }
  const montage = await presentation.export({ format: "webp", montage: true, scale: 1 });
  await writeBlob(path.join(PREVIEW_DIR, "deck-montage.webp"), montage);
  const qaText = [
    "视觉检查清单：",
    "- 已逐页导出 PNG 预览。",
    "- 已导出每页 layout JSON 便于复查文字与对象位置。",
    "- 重点检查项：标题是否两行内可读、图片是否为局部有效裁切、图示是否表达真实逻辑、深色页是否仍有高对比可读性。",
    "- 人工复查建议：优先检查封面、互动页、矩阵图、时间轴页、历史时间轴页和最后总结页。",
  ].join("\n");
  await fs.writeFile(path.join(QA_DIR, "visual-qa.txt"), qaText, "utf8");
}

