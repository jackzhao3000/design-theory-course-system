function buildNotes(slide) {
  if (slide.interaction) {
    return buildInteractiveNotes(slide);
  }
  return [
    slide.context,
    `这页的核心结论是：${slide.summary}`,
    slide.reading ? `图示阅读顺序请按“${slide.reading}”来带学生看，不要直接跳到结论。` : "",
    slide.caseNote || "",
    slide.misunderstanding ? `这里最容易混淆的是：${slide.misunderstanding}` : "",
    slide.question ? `讲到后半段时可以追问学生：${slide.question}` : "",
    slide.bridge || "",
  ]
    .filter(Boolean)
    .join("");
}

function rgba(hex, alpha = 1) {
  const value = hex.replace("#", "");
  const r = Number.parseInt(value.slice(0, 2), 16);
  const g = Number.parseInt(value.slice(2, 4), 16);
  const b = Number.parseInt(value.slice(4, 6), 16);
  return { color: { red: r, green: g, blue: b, alpha } };
}

async function writeBlob(targetPath, blob) {
  await fs.writeFile(targetPath, new Uint8Array(await blob.arrayBuffer()));
}

function bufferToArrayBuffer(buffer) {
  return buffer.buffer.slice(buffer.byteOffset, buffer.byteOffset + buffer.byteLength);
}

